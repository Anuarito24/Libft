#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		m;

	i = 0;
	m = 0;
	while (s1[i] != '\0')
		i++;
	while (n > 0 && s2[m] != '\0')
	{
		s1[i] = s2[m];
		i++;
		m++;
		n--;
	}
	s1[i++] = '\0';
	return (s1);
}
